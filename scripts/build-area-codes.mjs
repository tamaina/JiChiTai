import { execFile } from 'node:child_process'
import { readFile, writeFile } from 'node:fs/promises'
import path from 'node:path'
import { promisify } from 'node:util'

const J_LIS_INDEX_URL =
  'https://www.j-lis.go.jp/spd/code-address/jititai-code.html'
const J_LIS_ORIGIN = 'https://www.j-lis.go.jp'
const generatedMunicipalitiesFile = path.resolve(
  'src/shared/data/generated-municipalities.ts',
)
const overridesFile = path.resolve('data/area-code-overrides.csv')
const outputFile = path.resolve('src/shared/data/generated-area-codes.ts')
const userAgent =
  'Mozilla/5.0 (compatible; JiChiTai municipality data builder; +https://github.com/tamaina/JiChiTai)'
const execFileAsync = promisify(execFile)

async function fetchText(url) {
  const { stdout } = await execFileAsync(
    'curl',
    [
      '--fail',
      '--location',
      '--silent',
      '--show-error',
      '--user-agent',
      userAgent,
      url,
    ],
    {
      maxBuffer: 10 * 1024 * 1024,
    },
  )
  return stdout
}

function targetMunicipalityCodes(source) {
  const match = source.match(
    /generatedMunicipalities: GeneratedMunicipalityRecord\[\] = (\[[\s\S]*\])\n/,
  )
  if (!match) throw new Error('Could not parse generated municipality data')
  const codes = [...match[1].matchAll(/\bcode:\s*'(\d{5})'/g)].map(
    (item) => item[1],
  )
  if (!codes.length) throw new Error('Municipality data contains no codes')
  return new Set(codes)
}

function prefectureUrls(indexHtml) {
  return [
    ...indexHtml.matchAll(
      /href="([^"]*\/spd\/code-address\/[^"]+)"[^>]*>[^<]*(?:都|道|府|県)内/g,
    ),
  ].map((match) => new URL(match[1], J_LIS_ORIGIN).href)
}

function decodeHtml(value) {
  return value
    .replace(/<[^>]+>/g, '')
    .replace(/&amp;/g, '&')
    .replace(/&nbsp;/g, ' ')
    .trim()
}

function recordsFromPrefecture(html, targets) {
  const records = []
  for (const row of html.matchAll(/<tr[^>]*>([\s\S]*?)<\/tr>/g)) {
    const cells = [...row[1].matchAll(/<td[^>]*>([\s\S]*?)<\/td>/g)].map(
      (match) => decodeHtml(match[1]),
    )
    if (cells.length < 6 || !/^\d{6}$/.test(cells[0])) continue
    const code = cells[0].slice(0, 5)
    if (!targets.has(code)) continue
    const primaryAreaCode = cells[5].match(/^(0\d{1,4})-/)?.[1]
    if (!primaryAreaCode) continue
    records.push({
      code,
      primaryAreaCode,
      areaCodes: [primaryAreaCode],
      representativePhone: cells[5],
    })
  }
  return records
}

function parseOverrides(csv) {
  return csv
    .trim()
    .split(/\r?\n/)
    .slice(1)
    .filter(Boolean)
    .map((line) => {
      const [code, areaCode, sourceUrl, note] = line.split(',')
      return { code, areaCode, sourceUrl, note }
    })
}

const municipalitySource = await readFile(generatedMunicipalitiesFile, 'utf8')
const targets = targetMunicipalityCodes(municipalitySource)
const indexHtml = await fetchText(J_LIS_INDEX_URL)
const urls = prefectureUrls(indexHtml)
if (urls.length !== 47)
  throw new Error(`Expected 47 J-LIS prefecture pages, found ${urls.length}`)

const pages = []
for (const url of urls) pages.push(await fetchText(url))
const records = pages.flatMap((html) => recordsFromPrefecture(html, targets))
const byCode = new Map(records.map((record) => [record.code, record]))
if (byCode.size !== records.length)
  throw new Error('J-LIS data contains duplicate municipality codes')
const overrides = parseOverrides(await readFile(overridesFile, 'utf8'))
for (const override of overrides) {
  const record = byCode.get(override.code)
  if (!record)
    throw new Error(
      `Area-code override has no primary record: ${override.code}`,
    )
  if (!/^0\d{1,4}$/.test(override.areaCode))
    throw new Error(`Invalid area code override: ${override.areaCode}`)
  if (!record.areaCodes.includes(override.areaCode))
    record.areaCodes.push(override.areaCode)
}

for (const record of records) {
  record.areaCodes.sort((first, second) => first.localeCompare(second, 'ja'))
  if (!record.areaCodes.includes(record.primaryAreaCode))
    throw new Error(`Primary area code is missing for ${record.code}`)
}

records.sort((first, second) => first.code.localeCompare(second.code))
const generated = `// Generated by scripts/build-area-codes.mjs. Do not edit.\nexport interface GeneratedAreaCodeRecord {\n  code: string\n  primaryAreaCode: string\n  areaCodes: string[]\n  representativePhone: string\n}\n\nexport const generatedAreaCodes: GeneratedAreaCodeRecord[] = ${JSON.stringify(records)}\n\nexport const areaCodeSources = ${JSON.stringify(
  {
    primary: J_LIS_INDEX_URL,
    overrides,
  },
)}\n`
await writeFile(outputFile, generated)
console.log(
  `Generated area codes for ${records.length}/${targets.size} municipalities.`,
)
