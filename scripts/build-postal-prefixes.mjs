import { readFile, writeFile } from 'node:fs/promises'
import path from 'node:path'
import { strFromU8, unzipSync } from 'fflate'

const JAPAN_POST_URL =
  'https://www.post.japanpost.jp/service/search/zipcode/download/utf/zip/utf_ken_all.zip'
const generatedMunicipalitiesFile = path.resolve(
  'src/shared/data/generated-municipalities.ts',
)
const outputFile = path.resolve('src/shared/data/generated-postal-prefixes.ts')
const designatedCities = new Map([
  ['札幌市', '01100'],
  ['仙台市', '04100'],
  ['さいたま市', '11100'],
  ['千葉市', '12100'],
  ['横浜市', '14100'],
  ['川崎市', '14130'],
  ['相模原市', '14150'],
  ['新潟市', '15100'],
  ['静岡市', '22100'],
  ['浜松市', '22130'],
  ['名古屋市', '23100'],
  ['京都市', '26100'],
  ['大阪市', '27100'],
  ['堺市', '27140'],
  ['神戸市', '28100'],
  ['岡山市', '33100'],
  ['広島市', '34100'],
  ['北九州市', '40100'],
  ['福岡市', '40130'],
  ['熊本市', '43100'],
])

function targetMunicipalityCodes(source) {
  const match = source.match(
    /generatedMunicipalities: GeneratedMunicipalityRecord\[\] = (\[[\s\S]*\])\n/,
  )
  if (!match) throw new Error('Could not parse generated municipality data')
  return new Set(
    [...match[1].matchAll(/\bcode:\s*'(\d{5})'/g)].map((item) => item[1]),
  )
}

function parseCsvLine(line) {
  const fields = []
  let value = ''
  let quoted = false
  for (const character of line) {
    if (character === '"') quoted = !quoted
    else if (character === ',' && !quoted) {
      fields.push(value)
      value = ''
    } else value += character
  }
  fields.push(value)
  return fields
}

function targetCode(sourceCode, municipalityName, targets) {
  if (targets.has(sourceCode)) return sourceCode
  for (const [cityName, parentCode] of designatedCities) {
    if (municipalityName.startsWith(cityName)) return parentCode
  }
  return null
}

const municipalitySource = await readFile(generatedMunicipalitiesFile, 'utf8')
const targets = targetMunicipalityCodes(municipalitySource)
const response = await fetch(JAPAN_POST_URL)
if (!response.ok)
  throw new Error(
    `${response.status} ${response.statusText}: ${JAPAN_POST_URL}`,
  )
const archive = unzipSync(new Uint8Array(await response.arrayBuffer()))
const csvFile = Object.entries(archive).find(([name]) => name.endsWith('.csv'))
if (!csvFile) throw new Error('Japan Post archive contains no CSV file')

const prefixesByCode = new Map()
for (const line of strFromU8(csvFile[1]).split(/\r?\n/)) {
  if (!line) continue
  const fields = parseCsvLine(line)
  const code = targetCode(fields[0], fields[7], targets)
  const postalCode = fields[2]?.replace(/\s/g, '')
  if (!code || !/^\d{7}$/.test(postalCode)) continue
  const prefixes = prefixesByCode.get(code) ?? new Set()
  prefixes.add(postalCode.slice(0, 3))
  prefixesByCode.set(code, prefixes)
}

const records = [...prefixesByCode]
  .map(([code, prefixes]) => ({
    code,
    prefixes: [...prefixes].sort(),
  }))
  .sort((first, second) => first.code.localeCompare(second.code))
const generated = `// Generated by scripts/build-postal-prefixes.mjs. Do not edit.
export interface GeneratedPostalPrefixRecord {
  code: string
  prefixes: string[]
}

export const generatedPostalPrefixes: GeneratedPostalPrefixRecord[] = ${JSON.stringify(records)}
export const postalPrefixSource = ${JSON.stringify(JAPAN_POST_URL)}
`
await writeFile(outputFile, generated)
console.log(
  `Generated postal prefixes for ${records.length}/${targets.size} municipalities.`,
)
